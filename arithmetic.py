# int/float
val_1 = 1
val_2 = 2

print(val_1 + val_2)
print(val_1 - val_2)
print(val_1 * val_2)
print(val_1 / val_2)
print(val_1 // val_2) # целочисленное деление
print(val_1 ** val_2) # возведение в степень

#string
val_1 = "left"
val_2 = "right"

print(val_1 + val_2)

print((val_1 + " ") * 8)

print("<", val_1 < val_2)
print(">", val_1 > val_2)
print("==", val_1 == val_2)
print(">=", val_1 >= val_2)
print("<=", val_1 <= val_2)
print("!=", val_1 != val_2)

